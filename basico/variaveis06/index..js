// Não é possível criar variáveis com palavras reservadas do JS
// Variáveis precisam ter nomes semânticos (Com sentido)
// Variáveis não podem começar com número. Não podem conter espaço ou traços
// É recomendado utilizar o padrão camel case (Nome do cliente = nomeCliente por exemplo)
// Letras maiúsculas e minúsculas tem diferença
// NÃO UTILIZAR VAR, apenas let e const.
let nome; // Declaração de variável, sem atribuir um valor a ela
nome = "Alexandre"; // atribuição de valor a variável
console.log(nome);
nome = "André"; // Modificação do valor da variável 'nome'
console.log(nome);
